package JavaSessions;

public class SOPConcept {

	public static void main(String[] args) {
		System.out.println();
		System.out.println("Naveen");
		System.out.println("Naveen" + 10);
		System.out.println(true);
		System.out.println('d');
		System.out.println(2);
		System.out.println(2 + 2);
		
		

	}
	/**
	 * Class System { 
	 * static PrintStream out;
	 *  }
	 *  System.out.println(); 
	 *  System is a default class 
	 *  out is a static field in System class with PrintStream class reference
	 *   println is an overloaded method defined in PrintStream class
	 */

}
